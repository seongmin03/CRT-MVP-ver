// 영문명을 국가명으로 역변환
export const englishToCountryName: Record<string, string> = Object.entries(countryNameToEnglish).reduce(
  (acc, [korean, english]) => {
    acc[english] = korean;
    return acc;
  },
  {} as Record<string, string>
);

// URL 코드를 국가명으로 변환
export const urlCodeToCountryName = (code: string): string | null => {
  // 유럽 전체 페이지
  if (code === "eu") {
    return "유럽";
  }
  return englishToCountryName[code] || null;
};

// 국가명을 URL 코드로 변환
export const countryNameToUrlCode = (countryName: string): string => {
  return countryNameToEnglish[countryName] || countryName.toLowerCase().replace(/\s+/g, '-');
};

